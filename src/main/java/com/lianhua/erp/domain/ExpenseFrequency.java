package com.lianhua.erp.domain;

/**
 * 費用類別頻率類型
 * 定義費用類別可新增的頻率限制
 */
public enum ExpenseFrequency {
    /**
     * 每日一次 - 同一天內，同一類別只能建立一筆支出記錄
     * 適用於：日常開支、每日固定費用等
     * 注意：此為預設值，與原有的同一天檢查邏輯一致
     */
    DAILY,
    
    /**
     * 每週一次 - 同一週（週一至週日）內，同一類別只能建立一筆支出記錄
     * 適用於：週租費、每週固定費用等
     */
    WEEKLY,
    
    /**
     * 每兩週一次 - 每兩週一個週期內，同一類別只能建立一筆支出記錄
     * 適用於：雙週薪、兩週一次的固定費用等
     */
    BIWEEKLY,
    
    /**
     * 每月一次 - 同一會計期間（YYYY-MM）內，同一類別只能建立一筆支出記錄
     * 適用於：房租、月租費等固定月費
     */
    MONTHLY,
    
    /**
     * 無限制 - 不限制頻率，但仍需遵守同一天同一類別的基本規則（避免完全重複）
     * 適用於：不固定頻率的費用，如臨時採購、特殊支出等
     */
    UNLIMITED
}

